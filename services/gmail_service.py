import base64
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
from email.mime.base import MIMEBase
from email import encoders
from pathlib import Path
from typing import Optional, Dict, Any


def send_email(
    gmail_service,
    recipient_email: str,
    subject: str,
    body_html: str
) -> Dict[str, Any]:
    """
    Send a simple HTML email using Gmail API.
    
    Args:
        gmail_service: Authenticated Gmail API service client
        recipient_email: Recipient's email address
        subject: Email subject line
        body_html: HTML content for email body
    
    Returns:
        dict: Response from Gmail API containing message ID
    
    Raises:
        Exception: If email sending fails
    """
    message = MIMEText(body_html, "html")
    message["to"] = recipient_email
    message["subject"] = subject
    
    raw = base64.urlsafe_b64encode(message.as_bytes()).decode()
    
    try:
        sent = gmail_service.users().messages().send(
            userId="me",
            body={"raw": raw}
        ).execute()
        print(f"üìß Email sent to {recipient_email} (Message ID: {sent['id']})")
        return sent
    except Exception as e:
        print(f"‚ùå Failed to send email: {e}")
        raise


